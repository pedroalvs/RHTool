-- Criar tabelas em RHTOOL
CREATE TABLE RHTOOL.TECNOLOGIA(
	IDTECNOLOGIA INT NOT NULL,
	NOME VARCHAR NOT NULL)
;

CREATE TABLE RHTOOL.[ROLE](
	IDROLE INT MOT NULL,
	NOME VARCHAR NOT NULL)
;

CREATE TABLE RHTOOL.CANDIDATO(
	IDCANDIDATO INT NOT NULL,
	NOME VARCHAR NOT NULL,
	IDADE INT NOT NULL,
	LINKEDIN VARCHAR NOT NULL,
	GUPY VARCHAR NOT NULL)
;

CREATE TABLE RHTOOL.VAGA(
	IDVAGA INT NOT NULL,
	NOME VARCHAR NOT NULL,
	CLIENTE VARCHAR NOT NULL,
	STATUS VARCHAR NOT NULL,
	GUPY VARCHAR NOT NULL)
;

CREATE TABLE RHTOOL.REQUIREMENT(
	IDTECNOLOGIA INT NOT NULL,
	IDVAGA INT NOT NULL,
	SENIORIDADE VARCHAR NOT NULL)
;

CREATE TABLE RHTOOL.HARDSKILL(
	IDCANDIDATO INT NOT NULL,
	IDTECNOLOGIA INT NOT NULL,
	EXPERIENCIA VARCHAR NOT NULL)
;

CREATE TABLE RHTOOL.TECROLE(
	IDTECNOLOGIA INT NOT NULL,
	IDROLE INT NOT NULL)
;
